package bean;

/**
 * 歌手实体
 * @author wxf
 *
 */
public class Artist {
	/**歌手编号*/
	private Integer id ;
	/**歌手姓名*/
	private String name ;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
